var items1 = [];
var re1 =0;


while( re1 <999 ) {
      function Do1() {
          items1.push(document.querySelector('.word1').value);
          document.querySelector(".things-display1").innerHTML =items1;
          document.querySelector(".items-display1").innerHTML = items1.length;
      };

      function Delete1(){
          items1.pop();
          document.querySelector(".things-display1").innerHTML = items1;
          document.querySelector(".items-display1").innerHTML = items1.length;
        };

        function Reset1(){
          document.querySelector(".things-display1").innerHTML = items1="  ";
          document.querySelector(".items-display1").innerHTML = items1.length=0;
        };

    re1++;
  }
